#!/usr/bin/env node
/**
 * generate-feature-registry — scans src/features/* and (re)writes
 * src/shell/feature-registry.generated.ts so prototype features auto-appear
 * in the sidebar and at /lab/<id> with zero edits to shared files.
 *
 * Runs automatically on `predev` and `prebuild`. Output is deterministic
 * (features sorted by id), so a merge conflict here is resolved by re-running it.
 */
const fs = require("fs");
const path = require("path");

const featuresDir = path.join(__dirname, "../src/features");
const outFile = path.join(__dirname, "../src/shell/feature-registry.generated.ts");

function isFeatureDir(name) {
  if (name.startsWith("_") || name.startsWith(".")) return false;
  const dir = path.join(featuresDir, name);
  if (!fs.statSync(dir).isDirectory()) return false;
  const hasConfig = fs.existsSync(path.join(dir, "feature.config.ts"));
  const hasIndex =
    fs.existsSync(path.join(dir, "index.tsx")) ||
    fs.existsSync(path.join(dir, "index.ts"));
  return hasConfig && hasIndex;
}

const features = fs.existsSync(featuresDir)
  ? fs.readdirSync(featuresDir).filter(isFeatureDir).sort()
  : [];

const imports = features
  .map((id, i) => `import config${i} from "@/features/${id}/feature.config";`)
  .join("\n");

const configList = features.map((_, i) => `  config${i},`).join("\n");

const loaderList = features
  .map((id) => `  ${JSON.stringify(id)}: () => import("@/features/${id}"),`)
  .join("\n");

const out = `// AUTO-GENERATED by scripts/generate-feature-registry.js — DO NOT EDIT BY HAND.
// Run \`npm run generate:features\` to regenerate.
// On a merge conflict in this file, just re-run that command — the output is deterministic.
import type { ComponentType } from "react";
import type { FeatureConfig } from "./types";
${imports ? `\n${imports}\n` : ""}
export const featureConfigs: FeatureConfig[] = [
${configList}
];

export const featureLoaders: Record<
  string,
  () => Promise<{ default: ComponentType }>
> = {
${loaderList}
};
`;

fs.writeFileSync(outFile, out);
console.log(
  `✓ feature registry: ${features.length} feature(s) [${features.join(", ") || "none"}]`
);
